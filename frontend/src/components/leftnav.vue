                :index="'/'+chmenu.attributes.url"
                :key="chmenu.id"
                @click="handleOpen2(chmenu)">
                <el-icon>
                  <component :is="chmenu.attributes.icon?chmenu.attributes.icon:'Menu'" />
                </el-icon>
                {{chmenu.text}}
              </el-menu-item>
              <el-sub-menu v-else :index="'/'+chmenu.attributes.url">
