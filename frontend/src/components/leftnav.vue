                  </el-icon>
                  {{cchmenu.text}}
                </el-menu-item>
              </el-sub-menu>
            </div>
          </el-menu-item-group>
        </el-sub-menu>
        <el-menu-item  v-else :index="'/'+menu.attributes.url" :key="menu.id" @click="handleOpen2(menu)">
          <el-icon v-if="menu.attributes.icon!=''">
            <component :is="menu.attributes.icon" />
          </el-icon>
          <template #title>{{menu.text}}</template>
        </el-menu-item>
      </div>
    </el-menu>
  </div>
